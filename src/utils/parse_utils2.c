#include "cub3d.h"

int	itter_no_spaceornl(char *str)
{
	int	i;

	i = 0;
	while (str[i] && (str[i] != ' ' && str[i] != '	'
			&& str[i] != '\v' && str[i] != '\n'))
		i++;
	return (i);
}
